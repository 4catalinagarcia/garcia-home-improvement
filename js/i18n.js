/* ---- State ---- */
let currentLang = localStorage.getItem('ghiLang') || 'en';

/* ---- Apply translations ---- */
function applyTranslations(lang) {
    const t = translations[lang];
    if (!t) return;

    /* textContent elements */
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) el.textContent = t[key];
    });

    /* innerHTML elements (contain <br> or HTML tags) */
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        if (t[key] !== undefined) el.innerHTML = t[key];
    });

    /* Update <html> lang attribute */
    document.documentElement.lang = lang;

    /* Update toggle button labels to show the OTHER language */
    const otherLabel = lang === 'en' ? 'Español' : 'English';

    const label = document.getElementById('langLabel');
    if (label) label.textContent = otherLabel;

    const labelMobile = document.getElementById('langLabelMobile');
    if (labelMobile) labelMobile.textContent = otherLabel;

    /* Persist choice */
    localStorage.setItem('ghiLang', lang);
    currentLang = lang;
}

/* ---- Toggle buttons ---- */
function toggleLang() {
    applyTranslations(currentLang === 'en' ? 'es' : 'en');
}

const langToggle = document.getElementById('langToggle');
if (langToggle) langToggle.addEventListener('click', toggleLang);

const langToggleMobile = document.getElementById('langToggleMobile');
if (langToggleMobile) langToggleMobile.addEventListener('click', toggleLang);

/* ---- Apply on page load ---- */
applyTranslations(currentLang);
